public class pat3
{
    public static void main(String args[])
    {
        for(int i=1;i<=3;i++)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print(i+"");
            }
            System.out.println();
        }
        for(int i=2;i>=2;i++)
        {
            for(int j=i;j<=2;j++)
            {
                System.out.print(i+"");
            }
            System.out.println();
        }
        
    }
}